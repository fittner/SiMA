package datageneration.datatypes;

public class clsStepData {
	private int mrStep;
	
	public clsStepData(int prStep) {
		setStep(prStep);
	}

	
	
	/**
	 * @return the mrStep
	 */
	public int getStep() {
		return mrStep;
	}

	/**
	 * @param mrStep the mrStep to set
	 */
	public void setStep(int prStep) {
		this.mrStep = prStep;
	}
}
